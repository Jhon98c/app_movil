                  Navigator.of(context).pushReplacement(
                      MaterialPageRoute(builder: (context) => bottomNavBar()));
                },
                color: Colors.white, //blue[900],
                child:
                    Text('Empezar', style: TextStyle(color: Colors.blue[900])),
              ),
            )

          /*Container(
              height: 100.0,
